	TABLE QLPN.PN_TIEN_SUS;

INSERT
	INTO
	QLPN.PN_TIEN_SUS
(
	TS_SO_QUYET_DINH,
	TS_NGAY_QUYET_DINH,
	TS_HANH_VI_VI_PHAM,
	TS_DON_VI_XU_LY,
	TS_HINH_THUC_XU_LY,
	TS_NOI_CHAP_HANH,
	TS_NGAY_BAT_DAU,
	TS_NGAY_KET_THUC,
	TS_GHI_CHU,
	TS_THOI_GIAN,
	TS_PN_LAI_LICH_ID,
	CREATION_TIME,
	CREATOR_USER_ID,
	LAST_MODIFICATION_TIME,
	LAST_MODIFIER_USER_ID,
	IS_DELETED,
	DELETER_USER_ID,
	DELETION_TIME)
