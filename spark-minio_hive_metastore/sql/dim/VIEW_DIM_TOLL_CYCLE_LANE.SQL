
CREATE VIEW VIEW_DIM_TOLL_CYCLE_LANE
AS
SELECT B.CYCLE_ID, B.CYCLE_CODE, B.CYCLE_NAME
, C.TOLL_ID, C.TOLL_NAME
, D.LANE_CODE, D.LANE_NAME
FROM CSDL_RATING_OWNER.TOLL_CYCLE A
LEFT JOIN CSDL_RATING_OWNER.CLOSED_CYCLE B ON A.CYCLE_ID = B.CYCLE_ID 
LEFT JOIN CSDL_RATING_OWNER.TOLL C ON A.TOLL_ID = C.TOLL_ID 
LEFT JOIN CSDL_RATING_OWNER.TOLL_LANE D ON A.TOLL_ID = D.TOLL_ID ;